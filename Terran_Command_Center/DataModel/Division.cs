using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Terran_Command_Center.DataModel
{
    public class Division
    {
        public String _Name { get; set; }
        public List<Department> _Departments { get; set; }
        public String _Description { get; set; }
        public String _VFP { get; set; }
        public Guid _ID { get; set; }


        public Division(String Name, List<Department> Departments, String Description, String VFP,  Guid ID)
        {
            this._Name = Name;
            this._Departments = Departments;
            this._Description = Description;
            this._VFP = VFP;
            this._ID = ID;
        }
    }
}