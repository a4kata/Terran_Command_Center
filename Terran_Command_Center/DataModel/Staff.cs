using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Terran_Command_Center.DataModel
{
    public class Staff
    {
        public String _Name { get; set; }
        public String _Email { get; set; }
        public int _Phone { get; set; }
        public Byte[] _Photo { get; set; }
        public Guid _ID { get; set; }


        public Staff(String Name, String Email, int Phone, byte[] Photo, Guid ID)
        {
            this._Name = Name;
            this._Email = Email;
            this._Phone = Phone;
            this._Photo = Photo;
            this._ID = ID;
        }
    }
}